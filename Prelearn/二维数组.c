#include <stdio.h>
int main()
{
    //a为三行五列的矩阵  列必须定义出数组大小，行不用
    int a[3][5];
    //二维数组的初始化
    int b[][5] = {          // *列数是必须给出的，行数可以由编译器来数
        {0, 1, 2, 3, 4},    
        {2, 3, 4, 5, 6},
    };

    int i, j;
    //  二维数组的遍历
    for (i = 0; i < 3; i++)
    {
        for (j = 0; j < 5; j++)
        {
            a[i][j] = i * j; //[i][j]是一个int   表示i行j列上的单元
        }
    }

    return 0;
}

